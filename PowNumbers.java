public class PowNumbers {
    public static void main(String[] args)
    {
        System.out.printf("%.0f, ", Math.pow(-20, 2));
        System.out.printf("%.0f, ", Math.pow(92, 2));
        System.out.printf("%.0f, ", Math.pow(638, 2));
        System.out.printf("%.0f, ", Math.pow(73, 2));
        System.out.printf("%.0f, ", Math.pow(200, 2));
        System.out.printf("%.0f", Math.pow(-22, 2));
    }
}
