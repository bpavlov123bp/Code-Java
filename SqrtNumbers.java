public class SqrtNumbers {
    public static void main(String[] args)
    {
        System.out.printf("%.2f\t", Math.sqrt(8));
        System.out.printf("%.2f\t", Math.sqrt(353));
        System.out.printf("%.2f\t", Math.sqrt(78));
        System.out.printf("%.2f\t", Math.sqrt(3));
        System.out.printf("%.2f\t", Math.sqrt(Math.abs(-67)));
        System.out.printf("%.2f\t", Math.sqrt(723));
        System.out.printf("%.2f\t", Math.sqrt(2000));
        System.out.printf("%.2f\t", Math.sqrt(Math.abs(-100)));
    }
}
